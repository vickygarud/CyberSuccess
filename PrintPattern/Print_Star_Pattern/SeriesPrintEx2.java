package Print_Star_Pattern;

public class SeriesPrintEx2 {
	public static void main(String[] args) {
		
		int cnt=10;
		System.out.print("Print ");
		
		while(cnt<=40)	// 10 <= 40
		{
			System.out.print(cnt+""+cnt+" ");  // 1010  
			cnt+=10;
		}
		
		//System.out.println("Last Loop: "+cnt); 50
		
		
	}

}




