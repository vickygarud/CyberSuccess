package Print_Star_Pattern;

public class starprint {

	public static void main(String[] args) {
		
		for(int i=1; i<=5; i++)		// 2
		{
			
			for(int j=1; j<=i; j++)				// 1<1 2 |   1<=2  |  1<=3
			{
				System.out.print("*");
			}
			
			System.out.println();
			
			
		}
	
	
	}

}


