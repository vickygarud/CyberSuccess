package Day_Dec07;

public class DoWhileLoop {

	public static void main(String[] args) {
		
		int i=1;
		do
		{
			System.out.print(i+" ");
			i++;
		} while(i<=5);
		
		System.out.println(" / Out of do while Loop: "+i);
		
		
		
		
		System.out.println("-------------------------------");
		
		
		
		i=1;
		while(i<=5)
		{
			System.out.print(i+" ");
			i++;		
		}	
		
		System.out.println(" / Out of while Loop: "+i);
		
		
		
		System.out.println("---------------------------------");
		
		
		
		for(i=1; i<=5; i++)
		{
			System.out.print(i+" ");
		}
		System.out.println(" / out of for Loop: "+i);
		
		

	}

}
